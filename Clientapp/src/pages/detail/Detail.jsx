import { useContext, useState } from "react";
import Navbar from "../home/Navbar/Navbar";
import FormRegister from "../home/FormRegister/FormRegister";
import Footer from "../home/Footer/Footer";
import { FontAwesomeIcon } from "@fortawesome/react-fontawesome";
import {
  faCircleArrowLeft,
  faCircleArrowRight,
  faCircleXmark,
  faLocationDot,
} from "@fortawesome/free-solid-svg-icons";
import { useLocation, useNavigate } from "react-router-dom";
import useFetch from "../../hooks/useFetch";
import { SearchContext } from "../../context/SearchContext";
import styles from "./Detail.module.css";
import { AuthContext } from "../../context/AuthContext";
import Reserve from "./reserve/Reserve";

//Component trang Detail
const Detail = () => {
  const location = useLocation();
  const id = location.pathname.split("/")[2];
  const [slideNumber, setSlideNumber] = useState(0);
  const [open, setOpen] = useState(false);
  const [openModal, setOpenModal] = useState(false);

  const { data, loading } = useFetch(`/hotels/find/${id}`);

  const { user } = useContext(AuthContext);
  const navigate = useNavigate();

  const { dates, options } = useContext(SearchContext);

  const MILLISECONDS_PER_DAY = 1000 * 60 * 60 * 24;
  function dayDifference(date1, date2) {
    const timeDiff = Math.abs(date2?.getTime() - date1?.getTime());
    const diffDays = Math.ceil(timeDiff / MILLISECONDS_PER_DAY);
    return diffDays;
  }

  const days = dayDifference(dates[0]?.endDate, dates[0]?.startDate);

  const handleOpen = (i) => {
    setSlideNumber(i);
    setOpen(true);
  };

  const handleMove = (direction) => {
    let newSlideNumber;

    if (direction === "l") {
      newSlideNumber =
        slideNumber === 0 ? data.photos.length - 1 : slideNumber - 1;
    } else {
      newSlideNumber =
        slideNumber === data.photos.length - 1 ? 0 : slideNumber + 1;
    }

    setSlideNumber(newSlideNumber);
  };

  const getMinPriceFromRooms = (RoomArr) => {
    return RoomArr?.sort((a, b) => a.price - b.price)[0].price;
  };

  const handleClick = () => {
    if (user) {
      setOpenModal(true);
    } else {
      navigate("/login");
    }
  };

  return (
    <div>
      <div>
        <Navbar />
        {loading ? (
          <p>Loading...</p>
        ) : (
          <div className={styles.hotelContainer}>
            {open && (
              <div className={styles.slider}>
                <FontAwesomeIcon
                  icon={faCircleXmark}
                  className={styles.close}
                  onClick={() => setOpen(false)}
                />
                <FontAwesomeIcon
                  icon={faCircleArrowLeft}
                  className={styles.arrow}
                  onClick={() => handleMove("l")}
                />
                <div className={styles.sliderWrapper}>
                  <img
                    src={data.photos[slideNumber]}
                    alt=""
                    className={styles.sliderImg}
                  />
                </div>
                <FontAwesomeIcon
                  icon={faCircleArrowRight}
                  className={styles.arrow}
                  onClick={() => handleMove("r")}
                />
              </div>
            )}
            <div className={styles.hotelWrapper}>
              <button className={styles.bookNow} onClick={handleClick}>
                Reserve or Book Now!
              </button>
              <h1 className={styles.hotelTitle}>{data.name}</h1>
              <div className={styles.hotelAddress}>
                <FontAwesomeIcon icon={faLocationDot} />
                <span>{data.address}</span>
              </div>
              <span className={styles.hotelDistance}>
                Excellent location â€“ {data.distance}m from center
              </span>
              <span className={styles.hotelPriceHighlight}>
                Book a stay over ${data.cheapestPrice} at this property and get
                a free airport taxi
              </span>
              <div className={styles.hotelImages}>
                {data.photos?.map((photo, i) => (
                  <div className={styles.hotelImgWrapper} key={i}>
                    <img
                      onClick={() => handleOpen(i)}
                      src={photo}
                      alt=""
                      className={styles.hotelImg}
                    />
                  </div>
                ))}
              </div>
              <div className={styles.hotelDetails}>
                <div className={styles.hotelDetailsTexts}>
                  <h1 className={styles.hotelTitle}>{data.title}</h1>
                  <p className={styles.hotelDesc}>{data.decs}</p>
                </div>
                <div className={styles.hotelDetailsPrice}>
                  <h1>Perfect for a {days || 1}-night stay!</h1>
                  <span>
                    Located in the real heart of Krakow, this property has an
                    excellent location score of 9.8!
                  </span>
                  <h2>
                    <b>
                      $
                      {days && options
                        ? days * getMinPriceFromRooms(data.rooms) * options.room
                        : getMinPriceFromRooms(data.rooms)}
                    </b>{" "}
                    ({days || 1} nights)
                  </h2>
                  <button onClick={handleClick}>Reserve or Book Now!</button>
                </div>
              </div>
            </div>
          </div>
        )}
        <FormRegister />
        <Footer />
      </div>
      {openModal && <Reserve setOpen={setOpenModal} hotelId={id} />}
    </div>
  );
};

export default Detail;
